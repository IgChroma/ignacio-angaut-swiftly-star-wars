/**
 * @description: * recieves a color and a svg from list of possible planet types 
 *  desert_planet: For desert planets.
    forest_planet: For temperate or forest-like planets.
    industrial_planet: For industrial worlds.
    swamp_planet: For swampy or wetland planets.
    ocean_planet: For ocean or water-dominated planets.
    rocky_planet: For rocky or mountainous planets.
    volcanic_planet: For volcanic planets.
    gas_giant_planet: For gas giants.
    ice_planet: For ice planets.
    cityscape_planet: For planets dominated by cities.
 */
export const PlanetLogo = ({
  planetData
}: {
  planetData: PlanetVisualCharacteristics;
}) => {
  const { color, svg } = planetData;

  if (
    [
      "desert_planet",
      "forest_planet",
      "swamp_planet",
      "rocky_planet",
    ].indexOf(svg) > -1
  ) {
    return <Planet1SVG color={color} />;
  }

  if (["industrial_planet", "cityscape_planet", "volcanic_planet"].indexOf(svg) > -1) {
    return <Planet2SVG color={color} />;
  }

  if (["ice_planet", "gas_giant_planet", "ocean_planet"].indexOf(svg) > -1) {
    return <Planet3SVG color={color} />;
  }

  return <Planet3SVG />;
};

